#[cfg(test)]
mod tests {
    use super::*;
    use std::collections::BTreeMap;
    use terrain::prelude::{
        EdgeId, TerrainTileDefinition, TerrainTileSet, TilePattern, TileVariant, TintRange,
    };

    fn empty_tileset(fn_name_key: &str) -> TerrainTileSet {
        let def = TerrainTileDefinition {
            name: fn_name_key.to_owned(),
            variants: [
                TileVariant {
                    pattern: TilePattern::Solid,
                    shapes: vec![],
                    edge_ids: [EdgeId::NONE; 4],
                },
                TileVariant {
                    pattern: TilePattern::OuterCorner,
                    shapes: vec![],
                    edge_ids: [EdgeId::NONE; 4],
                },
                TileVariant {
                    pattern: TilePattern::Edge,
                    shapes: vec![],
                    edge_ids: [EdgeId::NONE; 4],
                },
                TileVariant {
                    pattern: TilePattern::Diagonal,
                    shapes: vec![],
                    edge_ids: [EdgeId::NONE; 4],
                },
                TileVariant {
                    pattern: TilePattern::InnerCorner,
                    shapes: vec![],
                    edge_ids: [EdgeId::NONE; 4],
                },
            ],
            priority: 0,
            tint_range: TintRange {
                hue_shift_max: 5.0,
                brightness_shift_max: 0.05,
            },
        };
        TerrainTileSet {
            tiles: BTreeMap::from([(fn_name_key.to_owned(), def)]),
            adjacency_rules: Vec::new(),
        }
    }

    #[test]
    fn when_generate_then_contains_pub_fn_tileset() {
        // Arrange
        let tileset = empty_tileset("grass");
        // Act
        let code = generate_tileset_code(&tileset, "tileset", "terrain.tsx");
        // Assert
        assert!(
            code.contains("pub fn tileset()"),
            "missing pub fn tileset()"
        );
    }

    #[test]
    fn when_generate_then_contains_terrain_tileset_constructor() {
        let tileset = empty_tileset("grass");
        let code = generate_tileset_code(&tileset, "tileset", "terrain.tsx");
        assert!(code.contains("TerrainTileSet"), "missing TerrainTileSet");
    }

    #[test]
    fn when_generate_then_contains_terrain_prelude_import() {
        let tileset = empty_tileset("grass");
        let code = generate_tileset_code(&tileset, "tileset", "terrain.tsx");
        assert!(
            code.contains("use terrain::prelude::*"),
            "missing terrain prelude import"
        );
    }

    #[test]
    fn when_custom_fn_name_then_uses_it() {
        let tileset = empty_tileset("grass");
        let code = generate_tileset_code(&tileset, "my_tileset", "terrain.tsx");
        assert!(
            code.contains("pub fn my_tileset()"),
            "missing custom fn name"
        );
    }
}
